    public frmCaixaDisponiveis(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        if(InicializarUI())
        {
            //dbgCaixa.requestFocus();
            this.setLocationRelativeTo(null);
            try {
                dbgCaixa.getRsDados().beforeFirst();
                if (!dbgCaixa.getRsDados().next()) {
                    this.dispose();
                } else {
                    
                    
                    this.dbgCaixa.getjTable().requestFocus();
                    if(this.dbgCaixa.getjTable().getRowCount()>0){
                        this.dbgCaixa.getjTable().setRowSelectionInterval(0, 0);
                    }
                    this.update(this.getGraphics());
                    this.setVisible(true);                    
                }
            } catch (SQLException ex) {
               LogDinnamus.Log(ex);
            }
            
        }
        else
        {
            this.dispose();
        }
    }
    protected boolean InicializarUI()
    {
        boolean bRet=false;

        try {
            String cSql="select c.codigo CodCaixa, c.nome as Caixa, u.Codigo as CodigoUs , u.nome Usuario " +
	  "from caixas c " +
		"inner join usuario u " +
			"on u.codigo=c.operadorcx " +
                "inner join filial f "+
                " on f.codigofilial=c.filial " +
            "where c.status='A' and f.codigoloja=" + Sistema.getCodigoLojaMatriz();
            dbgCaixa.getjTable().getTableHeader().setResizingAllowed(false);
            dbgCaixa.getjTable().getTableHeader().setReorderingAllowed(false);            
            dbgCaixa.setRsDados( DAO_RepositorioLocal.GerarResultSet(cSql, ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY));
            dbgCaixa.TamanhoColunas(new int[] {50,200,50,200});
            dbgCaixa.update(dbgCaixa.getGraphics());
            dbgCaixa.getjTable().addKeyListener(
            new KeyListener() {
                public void keyTyped(KeyEvent e) {
                    if(e.getKeyChar()=='\n')
                    {
                        //SelecionarCaixa(dbgCaixa.TratarLinhaSelecionada(dbgCaixa.getjTable()));
                        btAbrirActionPerformed(null);
                        e.consume();
                    }
                }
                public void keyPressed(KeyEvent e) {
                if(e.getKeyCode()==KeyEvent.VK_ENTER )
                    e.consume();
                }
                public void keyReleased(KeyEvent e) {}
                }
            );

            bRet=true;
        } catch (Exception e) {
            LogDinnamus.Log(e);
        }

        return bRet;
    }

