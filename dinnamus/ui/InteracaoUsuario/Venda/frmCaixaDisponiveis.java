    protected boolean SelecionarCaixa(Object[] LinhaSelecionada)
    {
        boolean bRet=false;
        try {
            //Object[] LinhaSelecionada= dbgCaixa.TratarLinhaSelecionada(dbgCaixa.getjTable());
            if(LinhaSelecionada!=null)
            {
                UsuarioSistema.setIdCaixaAtual(Integer.valueOf(LinhaSelecionada[0].toString()));
                UsuarioSistema.setIdUsuarioLogadoCaixa(Integer.valueOf(LinhaSelecionada[2].toString()));
                bRet=true;
            }
        } catch (Exception e) {
            LogDinnamus.Log(e);
        }
        return bRet;
    }
