    private void btNFCe_ConsultarStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNFCe_ConsultarStatusActionPerformed
        // TODO add your handling code here:
        if(!btNFCe_ConsultarStatus.isEnabled())  {return;}
        try {
            if (pdv.isNFCe_OK()) {

                MetodosUI_Auxiliares_1.BloquearDesbloquearComponentes(PainelPrincipal, false);
                MetodosUI_Auxiliares_1.BloquearDesbloquearComponentes(PainelNFCe, false);
                InterromperCapturaTeclas = true;
                new Thread("btNFCe_ConsultarStatusActionPerformed") {
                    @Override
                    public void run() {
                        String Ret = new NFCE_ConsultarStatus().Consultar_NFCe(false, pdvgerenciar.CodigoPDV());                        
                        if (!Ret.equalsIgnoreCase("")) {                            
                            MetodosUI_Auxiliares_1.MensagemAoOperadorTextual("NFC-e : Consulta Status", "Retorno Sefaz", Ret, "INFO", new Font("Courier New", Font.PLAIN, 14));                        
                        } else {
                            MetodosUI_Auxiliares_1.MensagemAoUsuarioSimplesAVISO("Não foi possível consultar o status da NFC-e", "NFC-e Status");                              
                        }                        
                        MetodosUI_Auxiliares_1.BloquearDesbloquearComponentes(PainelPrincipal, true);
                        MetodosUI_Auxiliares_1.BloquearDesbloquearComponentes(PainelNFCe, true);
                        InterromperCapturaTeclas = false;
                        //btFecharActionPerformed(null);
                    }
                }.start();
            } else {
                MetodosUI_Auxiliares_1.MensagemAoUsuarioSimplesAVISO("A NFC-e não esta disponível", "NFC-e Status");
            }
        } catch (Exception e) {
            LogDinnamus.Log(e, true);
        }
        
    }//GEN-LAST:event_btNFCe_ConsultarStatusActionPerformed

